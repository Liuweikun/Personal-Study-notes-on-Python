#奥运五环

import turtle

turtle.showturtle()
turtle.width(30)
turtle.color("black")
turtle.circle(100)

turtle.penup()
turtle.goto(260,0)
turtle.color("red")
turtle.pendown()
turtle.circle(100)

turtle.penup()
turtle.goto(-260,0)
turtle.color("blue")
turtle.pendown()
turtle.circle(100)

turtle.penup()
turtle.goto(130,-100)
turtle.color("green")
turtle.pendown()
turtle.circle(100)

turtle.penup()
turtle.goto(-130,-100)
turtle.color("yellow")
turtle.pendown()
turtle.circle(100)


turtle.done()






 
 

