import turtle               #导入turtle模块
turtle.showturtle()         #显示箭头
turtle.write("白小纯")       #写字符串
turtle.color("red")         #画笔颜色改红色
turtle.forward(300)         #前进300像素
turtle.left(90)             #箭头左转90度
turtle.forward(300)         #
turtle.goto(0,100)           #去坐标点（0，50）
turtle.goto(0,0)            #
turtle.penup()              #抬笔
turtle.goto(100,100)        #
turtle.pendown()            #落笔
turtle.circle(100)          #半径为100画圆


